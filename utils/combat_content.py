def get_battlerdan_debate(topic_id: int) -> dict | None:
    for debate in BATTLERDAN_DEBATES:
        if int(debate.get("id", 0)) == int(topic_id):
            return debate
    return None


def get_battlerdan_option(topic_id: int, option_id: int) -> dict | None:
    debate = get_battlerdan_debate(topic_id)
    if not debate:
        return None
    for option in debate.get("options", []):
        if int(option.get("id", 0)) == int(option_id):
            return option
    return None
